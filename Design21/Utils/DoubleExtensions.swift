import Foundation

extension Double {
    static let twoPi: Double = 2 * Double.pi
    static let piOver2 = 0.5 * Double.pi
    static let piOver4 = 0.25 * Double.pi
    static let eps = 1e-12
    static let goldenRatio = (0.5 * (1.0 + sqrt(5.0)))
    static let log2 = log(2.0)
}

